'use client'

import { useState } from 'react'

import { Copy, Download, ExternalLink } from 'lucide-react'
import { toast } from 'sonner'

import type { DeepResearchResult } from '@/lib/tools/deep-research'

import { Button } from './ui/button'
import {
  Card,
  CardContent,
  CardDescription,
  CardHeader,
  CardTitle
} from './ui/card'

interface DeepResearchResultProps {
  result: DeepResearchResult
}

export function DeepResearchResultCard({ result }: DeepResearchResultProps) {
  const [isExporting, setIsExporting] = useState(false)

  const handleCopy = async () => {
    const text = `
# Deep Research: ${result.query}

## 摘要
${result.summary}

## 关键发现
${result.keyFindings.map((f, i) => `${i + 1}. ${f}`).join('\n')}

## 趋势与结论
${result.insights.map((i, idx) => `${idx + 1}. ${i}`).join('\n')}

## 参考来源
${result.sources.map((s, i) => `${i + 1}. [${s.title}](${s.url})`).join('\n')}
    `.trim()

    await navigator.clipboard.writeText(text)
    toast.success('已复制到剪贴板')
  }

  const handleExportMarkdown = () => {
    const markdown = `
# Deep Research: ${result.query}

## 摘要
${result.summary}

## 关键发现
${result.keyFindings.map((f, i) => `${i + 1}. ${f}`).join('\n')}

## 趋势与结论
${result.insights.map((i, idx) => `${idx + 1}. ${i}`).join('\n')}

## 参考来源
${result.sources.map((s, i) => `${i + 1}. [${s.title}](${s.url})${s.snippet ? `\n   > ${s.snippet}` : ''}`).join('\n\n')}

---
*Generated by Deep Research - ${new Date().toLocaleDateString('zh-CN')}*
    `.trim()

    const blob = new Blob([markdown], { type: 'text/markdown' })
    const url = URL.createObjectURL(blob)
    const a = document.createElement('a')
    a.href = url
    a.download = `deep-research-${result.query.replace(/\s+/g, '-')}-${Date.now()}.md`
    document.body.appendChild(a)
    a.click()
    document.body.removeChild(a)
    URL.revokeObjectURL(url)
    toast.success('已导出为 Markdown')
  }

  const handleExportPDF = async () => {
    setIsExporting(true)
    try {
      // Implement PDF export similar to the existing PDF export functionality
      toast.info('PDF 导出功能开发中')
    } catch (error) {
      toast.error('导出失败')
    } finally {
      setIsExporting(false)
    }
  }

  return (
    <Card className="w-full">
      <CardHeader>
        <div className="flex items-start justify-between">
          <div>
            <CardTitle className="text-xl">Deep Research 结果</CardTitle>
            <CardDescription className="mt-1">
              查询：{result.query} • 完成 {result.steps} 步调研
            </CardDescription>
          </div>
          <div className="flex gap-2">
            <Button
              variant="outline"
              size="sm"
              onClick={handleCopy}
              title="复制"
            >
              <Copy className="h-4 w-4" />
            </Button>
            <Button
              variant="outline"
              size="sm"
              onClick={handleExportMarkdown}
              title="导出 Markdown"
            >
              <Download className="h-4 w-4 mr-1.5" />
              MD
            </Button>
            <Button
              variant="outline"
              size="sm"
              onClick={handleExportPDF}
              disabled={isExporting}
              title="导出 PDF"
            >
              <Download className="h-4 w-4 mr-1.5" />
              PDF
            </Button>
          </div>
        </div>
      </CardHeader>

      <CardContent className="space-y-6">
        {/* Summary */}
        {result.summary && (
          <div>
            <h3 className="text-lg font-semibold mb-2">摘要</h3>
            <p className="text-sm text-muted-foreground leading-relaxed">
              {result.summary}
            </p>
          </div>
        )}

        {/* Key Findings */}
        {result.keyFindings && result.keyFindings.length > 0 && (
          <div>
            <h3 className="text-lg font-semibold mb-2">关键发现</h3>
            <ul className="space-y-2">
              {result.keyFindings.map((finding, index) => (
                <li key={index} className="flex gap-2 text-sm">
                  <span className="text-blue-600 dark:text-blue-400 font-medium">
                    {index + 1}.
                  </span>
                  <span className="text-muted-foreground">{finding}</span>
                </li>
              ))}
            </ul>
          </div>
        )}

        {/* Insights */}
        {result.insights && result.insights.length > 0 && (
          <div>
            <h3 className="text-lg font-semibold mb-2">趋势与结论</h3>
            <ul className="space-y-2">
              {result.insights.map((insight, index) => (
                <li key={index} className="flex gap-2 text-sm">
                  <span className="text-green-600 dark:text-green-400 font-medium">
                    {index + 1}.
                  </span>
                  <span className="text-muted-foreground">{insight}</span>
                </li>
              ))}
            </ul>
          </div>
        )}

        {/* Sources */}
        {result.sources && result.sources.length > 0 && (
          <div>
            <h3 className="text-lg font-semibold mb-2">参考来源</h3>
            <div className="space-y-3">
              {result.sources.map((source, index) => (
                <div
                  key={index}
                  className="flex items-start gap-2 p-3 rounded-lg bg-muted/50 hover:bg-muted transition-colors"
                >
                  <span className="text-xs text-muted-foreground font-medium mt-0.5">
                    {index + 1}
                  </span>
                  <div className="flex-1 min-w-0">
                    <a
                      href={source.url}
                      target="_blank"
                      rel="noopener noreferrer"
                      className="text-sm font-medium text-blue-600 dark:text-blue-400 hover:underline flex items-center gap-1 break-all"
                    >
                      {source.title}
                      <ExternalLink className="h-3 w-3 flex-shrink-0" />
                    </a>
                    {source.snippet && (
                      <p className="text-xs text-muted-foreground mt-1 line-clamp-2">
                        {source.snippet}
                      </p>
                    )}
                  </div>
                </div>
              ))}
            </div>
          </div>
        )}
      </CardContent>
    </Card>
  )
}
